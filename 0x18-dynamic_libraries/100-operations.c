#include <stdio.h>
/**
 * add - add two number
 * @a: first num
 * @b: second num
 * Return: sum
*/
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - sub two number
 * @a: first num
 * @b: second num
 * Return: a - b
*/
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - multiplucate two number
 * @a: first num
 * @b: second num
 * Return: a * b
*/
int mul(int a, int b)
{
	return (a * b);
}
/**
 * div - div two number
 * @a: first num
 * @b: second num
 * Return: a / b
*/
int div(int a, int b)
{
	return (a / b);
}
/**
 * mod - calculate mod
 * @a: first num
 * @b: second num
 * Return: a % b
*/
int mod(int a, int b)
{
	return (a % b);
}
